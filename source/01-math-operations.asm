; basic math operations

    mov ebx, 123 ; ebx = 123
    mov eax, ebx ; eax = ebx
    add ebx, ecx ; ebx += ecx
    sub ebx, edx ; ebx -= edx
    mul ebx      ; eax *= ebx
    mul edx      ; eax /= ebx

; `mov` instruction inserts data into a register
; multiplication and division operations are always appied to the EAX register.
